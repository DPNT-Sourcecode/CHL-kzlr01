package befaster.solutions.CHL;

class Result {
    private Integer q;
    private Integer r;

    Result(Integer q, Integer r) {
        this.q = q;
        this.r = r;
    }

    public Integer getQ() {
        return q;
    }

    public Integer getR() {
        return r;
    }

    public void setQ(Integer q) {
        this.q = q;
    }

    public void setR(Integer r) {
        this.r = r;
    }
}